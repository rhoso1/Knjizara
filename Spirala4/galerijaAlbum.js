function FullScreen1()
{
  var _slika = document.getElementById("Aslika1");
  
    if (_slika.requestFullscreen)
   {
    _slika.requestFullscreen();
	
   }

   else if (_slika.msRequestFullscreen) 
   {
    _slika.msRequestFullscreen();
   }
   else if (_slika.mozRequestFullScreen)
  {
    _slika.mozRequestFullScreen();
  }
  else if (_slika.webkitRequestFullscreen)
 {
  _slika.webkitRequestFullscreen();
 }	
}

